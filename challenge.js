// 카오스 이미지 풀 (chaos-000 ~ chaos-100)
const IMAGE_POOL_SIZE = 101;
function pickChaosImage() {
  const n = Math.floor(Math.random() * IMAGE_POOL_SIZE);
  const padded = String(n).padStart(3, '0');
  return `/카오스 추가 이미지/chaos-${padded}.png.jpeg`;
}

// 풀 셔플 (Fisher-Yates)
function shuffle(arr) {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

export default async function handler(req, res) {
  if (req.method !== 'POST') {
    return res.status(405).json({ status: 'error', message: 'POST only' });
  }
  
  try {
    // 챌린지 ID 발급
    const challengeId = randomUUID();
    
    // 풀 셔플 — 매 챌린지마다 순서 다름
    const rotationWords = shuffle(MUFE_POOL);
    
    // 카오스 이미지 무작위
    const chaosImage = pickChaosImage();
    
    // KV에 저장 (verify에서 풀 확인용, 5분 TTL)
    // verify.js가 이 풀을 읽어 caughtWord 유효성 검증
    await kv.set(`challenge:${challengeId}`, JSON.stringify({
      pool: MUFE_POOL,  // 전체 628 풀 (caughtWord 검증용)
      imageUrl: chaosImage,
      createdAt: Date.now(),
    }), { ex: 300 });  // 5분 후 자동 삭제
    
    return res.json({
      challengeId,
      rotationWords,    // 셔플된 628 깨끗한 한글
      chaosImage,
    });
  } catch (err) {
    console.error('[MUFE challenge] 실패:', err);
    return res.status(500).json({ status: 'error', message: '챌린지 발급 실패' });
  }
}
